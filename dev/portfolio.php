function portofolioTemplate($cnt, $image, $imageTitle, $imageAlt, $description) {
	$output = sprintf('

		<div class="respo-gal">
			<div class="gallery" value="%s">
				<img src="" data-src="%s" title="%s" alt="%s">
				<div class="desc">%s</div>
			</div>
		</div>
		',
		$cnt,
		$image,
		$imageTitle,
		$imageAlt,
		$description
	);

	return $output;
}

