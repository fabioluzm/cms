# 1 EN

function portfolioLang($language) {
	global $portfolioData;

	switch ($language) {
		case 0:
			$lang = 'pt';
			break;

		case 1:
			$lang = 'en';
			break;
	}

	$output = '';
	$cnt = 0;

	foreach ($portfolioData as $item) {
		$image = $item['image'];

		$description = $item[$lang]['description'];
		$imageAlt = $item[$lang]['imageAlt'];
		$imageTitle = $item[$lang]['imageTitle'];

		$output .= portofolioTemplate($cnt, $image, $imageTitle, $imageAlt, $description);

		$cnt++;
	}
	echo $output;
}

